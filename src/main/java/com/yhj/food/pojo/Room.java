package com.yhj.food.pojo;

public class Room {
    private Integer roomId;
    private String roomName;
    private String         roomMonth;
    private String roomPrise;
    private String   roomUrl;

    public Integer getRoomId() {
        return roomId;
    }

    public void setRoomId(Integer roomId) {
        this.roomId = roomId;
    }

    public String getRoomName() {
        return roomName;
    }

    public void setRoomName(String roomName) {
        this.roomName = roomName;
    }

    public String getRoomMonth() {
        return roomMonth;
    }

    public void setRoomMonth(String roomMonth) {
        this.roomMonth = roomMonth;
    }

    public String getRoomPrise() {
        return roomPrise;
    }

    public void setRoomPrise(String roomPrise) {
        this.roomPrise = roomPrise;
    }

    public String getRoomUrl() {
        return roomUrl;
    }

    public void setRoomUrl(String roomUrl) {
        this.roomUrl = roomUrl;
    }
}
